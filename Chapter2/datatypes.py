a=1 #integer
b=2.5 #float
c="hello" #string
d=True #boolean
e=None #null value
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
# to check the datatype of a variable use type() function
# integer, float, string, boolean, NoneType are the datatypes in python
# Python is a dynamically typed language, so you don't need to declare the datatype of a variable while assigning a value to it.
# The datatype of a variable is determined at runtime based on the value assigned to it.
