# arithmetic operators in python
a=10
b=3 # assignment operator
print("a + b =", a + b) # addition operator 
print("a - b =", a - b) # subtraction operator
print("a * b =", a * b) # multiplication operator   
print("a / b =", a / b) # division operator
print("a % b =", a % b) # modulus operator
print("a ** b =", a ** b) # exponentiation operator
print("a // b =", a // b) # floor division operator
# comparison operators in python
print("a == b:", a == b) # equal to operator
print("a != b:", a != b) # not equal to operator
print("a > b:", a > b) # greater than operator
print("a < b:", a < b) # less than operator
print("a >= b:", a >= b) # greater than or equal to operator
print("a <= b:", a <= b) # less than or equal to operator
# logical operators in python
x = True
y = False
print("x and y:", x and y) # logical and operator
print("x or y:", x or y) # logical or operator
print("not x:", not x) # logical not operator
# bitwise operators in python
p = 5  # binary: 0101
q = 3  # binary: 0011
print("p & q:", p & q) # bitwise and operator
print("p | q:", p | q) # bitwise or operator
print("p ^ q:", p ^ q) # bitwise xor operator
print("~p:", ~p) # bitwise not operator
print("p << 1:", p << 1) # left shift operator
print("p >> 1:", p >> 1) # right shift operator
# membership operators in python
list1 = [1, 2, 3, 4, 5]
print("3 in list1:", 3 in list1) # membership operator
print("6 not in list1:", 6 not in list1) # membership operator
# identity operators in python
m = 10
n = 10
print("m is n:", m is n) # identity operator
print("m is not n:", m is not n) # identity operator
# Note: Python supports operator overloading, which means that the same operator can have different meanings based on the context in which it is used.
# For example, the + operator can be used for both addition and string concatenation.
